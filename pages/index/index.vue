<template>
	<view class="content">
		<view class="navbar">
			<view 
				v-for="(item, index) in navList" :key="index" 
				class="nav-item" 
				:class="{current: tabCurrentIndex === index}"
				@click="tabClick(index)"
			>
				{{item.text}}
			</view>
		</view>

		<swiper :current="tabCurrentIndex" class="swiper-box" duration="300" @change="changeTab">
			<swiper-item class="tab-content" v-for="(tabItem,tabIndex) in navList" :key="tabIndex">
				<scroll-view 
					class="list-scroll-content" 
					scroll-y
					@scrolltolower="loadData"
				>
					<!-- 空白页 -->
					<empty v-if="tabItem.loaded === true && tabItem.videoList.length === 0"></empty>
					
					<!-- video列表 -->
					<view 
						v-for="(item,index) in tabItem.videoList" :key="index"
						class="video-item"
					>
							<view class="user-container">
							
								<image class="user-icon" :src="item.userface" mode="aspectFill"></image>
								
								<view class="user-name">
									<text>{{item.username}}</text>
								</view>
							</view>
							<view class="desc-container">
								<text>{{item.desc}}</text>
							</view>
							
							<view class="picture-container">
								<image class="picture" :src="item.image" @click="navToVideoPage({desc: '电商产品预计下周上架为双11奋斗'})" mode="aspectFill"></image>
							</view>
							
						</view>
					<uni-load-more :status="tabItem.loadingType"></uni-load-more>
					
				</scroll-view>
			</swiper-item>
		</swiper>
	</view>
</template> 

<script>
	import uniLoadMore from '@/components/uni-load-more/uni-load-more.vue';
	import empty from "@/components/empty";
	import Json from '@/Json';
	export default {
		components: {
			uniLoadMore,
			empty
		},
		data() {
			return {
				tabCurrentIndex: 0,
				navList: [{
						state: 0,
						text: '全部',
						loadingType: 'more',
						videoList: []
					},
					{
						state: 1,
						text: '时尚',
						loadingType: 'more',
						videoList: []
					},
					{
						state: 2,
						text: '美食',
						loadingType: 'more',
						videoList: []
					},
					{
						state: 3,
						text: '美妆',
						loadingType: 'more',
						videoList: []
					}
				],
			};
		},
		
		onLoad(options){
			/**
			 * 修复app端点击除全部订单外的按钮进入时不加载数据的问题
			 * 替换onLoad下代码即可
			 */
			this.tabCurrentIndex = +options.state;
			// #ifndef MP
			this.loadData()
			// #endif
			// #ifdef MP
			if(options.state == 0){
				this.loadData()
			}
			// #endif
			
		},
		 
		methods: {
			//获取订单列表
			loadData(source){
				//这里是将订单挂载到tab列表下
				let index = this.tabCurrentIndex;
				let navItem = this.navList[index];
				let state = navItem.state;
				
				if(source === 'tabChange' && navItem.loaded === true){
					//tab切换只有第一次需要加载数据
					return;
				}
				if(navItem.loadingType === 'loading'){
					//防止重复加载
					return;
				}
				
				navItem.loadingType = 'loading';
				
				setTimeout(()=>{
					let videoList = Json.videoList.filter(item=>{
						//添加不同状态下订单的表现形式
						item = Object.assign(item, this.videoStateExp(item.state));
						//演示数据所以自己进行状态筛选
						if(state === 0){
							//0为全部订单
							return item;
						}
						return item.state === state
					});
					videoList.forEach(item=>{
						navItem.videoList.push(item);
					})
					//loaded新字段用于表示数据加载完毕，如果为空可以显示空白页
					this.$set(navItem, 'loaded', true);
					
					//判断是否还有数据， 有改为 more， 没有改为noMore 
					navItem.loadingType = 'more';
				}, 600);	
			}, 

			//swiper 切换
			changeTab(e){
				this.tabCurrentIndex = e.target.current;
				this.loadData('tabChange');
			},
			//顶部tab点击
			tabClick(index){
				this.tabCurrentIndex = index;
			},
			
			//content状态文字和颜色
			videoStateExp(state){
				let stateTip = '',
					stateTipColor = '#fa436a';
				switch(+state){
					case 1:
						stateTip = '时尚'; break;
					case 2:
						stateTip = '美食'; break;
					case 3:
						stateTip = '美妆'; 
						stateTipColor = '#909399';
						break;
						
					//更多自定义
				}
				return {stateTip, stateTipColor};
			},
			// 跳转视频
			navToVideoPage(item) {
				// 跳转视频页面
				let id = item.desc;
				uni.navigateTo({
					url: `/pages/videoInfo/videoInfo?id=${id}`
				})
			}
		},
	}
</script>

